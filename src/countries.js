export function getCountry(code) {
    let index = COUNTRIES.findIndex(x => x.value === code);
    return COUNTRIES[index]; 
}

export function getContinent(iso)
{
  let continents = {};
  CONTINENTS.map(continent => {
    continent.sub_continent.map(sub_continent => {
      if(sub_continent.countries.findIndex(x => x===iso)>-1)
      {
        continents[continent.id] =  sub_continent.id;
        
      }
    })
  })
  return continents;
}